public class Literals {
        public static void main(String[] args) {
        
        // Integer literal
        int intLiteral = 100;

        // Floating-point literal
        double floatLiteral = 45.67;

        // Character literal
        char charLiteral = 'J';

        // String literal
        String stringLiteral = "Java Programming";

        // Boolean literal
        boolean booleanLiteral = true;

        // Printing all literals
        System.out.println(intLiteral);
        System.out.println(floatLiteral);
        System.out.println(charLiteral);
        System.out.println(stringLiteral);
        System.out.println(booleanLiteral);
    }
}


